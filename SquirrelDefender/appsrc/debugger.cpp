/********************************************************************************
 * Function: DebugTerm
 * Description: Class constructor.
 ********************************************************************************/
DebugTerm::DebugTerm(const std::string& terminal) : debug_terminal(terminal) 
{
    if (!debug_terminal.is_open()) 
    {
        std::cerr << "Failed to open terminal: " << terminal << std::endl;
        throw std::runtime_error("Failed to open terminal");
    }
}


/********************************************************************************
 * Function: ~DebugTerm
 * Description: Class destructor.
 ********************************************************************************/
DebugTerm::~DebugTerm(void)
{
    if (debug_terminal.is_open()) 
    {
        debug_terminal.close();
    }
}

/********************************************************************************
 * Function: Print
 * Description: Print statements to the additional terminal.
 ********************************************************************************/
void DebugTerm::Print(const std::string& message) 
{
    if (debug_terminal.is_open()) 
    {
        debug_terminal << message;
    }
}